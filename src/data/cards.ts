export const CARDS: Card[] = [
  ...majorArcana,
  ...wandsCards,
  ...cupsCards,
  ...swordsCards,
  ...pentaclesCards,
];

// 카드 ID로 카드 찾기
export function getCardById(id: string): Card | undefined {
  return CARDS.find((card) => card.id === id);
}

// 랜덤 카드 뽑기
export function drawRandomCard(): Card {
  const randomIndex = Math.floor(Math.random() * CARDS.length);
  return CARDS[randomIndex];
}

// 수트별 카드 가져오기
export function getCardsBySuit(suit: 'wands' | 'cups' | 'swords' | 'pentacles'): Card[] {
  return CARDS.filter((card) => card.suit === suit);
}

// 메이저 아르카나만 가져오기
export function getMajorArcana(): Card[] {
  return CARDS.filter((card) => card.type === 'major');
}

// 마이너 아르카나만 가져오기
export function getMinorArcana(): Card[] {
  return CARDS.filter((card) => card.type === 'minor');
}
